    {
        $request->validate([
            'uname'=> 'required|max:50',
            'email'=> 'required|max:100|unique:users',
            'mobile'=> 'numeric',
            'pass'=> 'required|min:6'

        ]);
        $data = User::insert([
            'name' => $request->uname,
            'email' => $request->email,
            'mobile' => $request->mobile,
